	GetCountryName: function (countryCode) {
		var cc = countryCode.toUpperCase();
		if (this.isoCountries.hasOwnProperty(cc)) {
			return this.isoCountries[cc];
		} else {
			return cc;
		}
	},

	GetLanguage: function (countryCode) {
		var cc = countryCode.toLowerCase();
		if (this.isoLanguages.hasOwnProperty(cc)) {
			return this.isoLanguages[cc];
		} else {
			return cc;
		}
	},
	FetchIso_3166_1FromLocale: function (locale) {
		return locale.split("-")[1];
	},
	FetchIso_639FromLocale: function (locale) {
		return locale.split("-")[0];
	},
